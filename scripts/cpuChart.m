load output_cpu_load_state.dump;
m1 = size(output_cpu_load_state, 1);
sampling = 25;

X = [0: sampling: sampling*(m1-1)];
p1 = plot(X, output_cpu_load_state(1:m1, 1));
set(p1,'Color','blue','LineWidth',1);
set(gca, ...
  'XMinorTick'  , 'on'      , ...
  'YMinorTick'  , 'on'      , ...
  'YGrid'       , 'on'      , ...
  'XGrid'       , 'on'      , ...
  'XColor'      , [.3 .3 .3], ...
  'YColor'      , [.3 .3 .3], ...
  'LineWidth'   , 1         );
hTitle = title('System Identification - Average CPU Load');
hXLabel = xlabel('Time (s)');
hYLabel = ylabel('Average CPU Load (%)');
set([hXLabel, hYLabel]  , ...
    'FontSize'   , 10          );
set( hTitle                    , ...
    'FontSize'   , 12          , ...
    'FontWeight' , 'bold'      );